     public void fetchClients() throws SQLException {
        clients = clientDataService.GetAll();

        DefaultComboBoxModel dml = new DefaultComboBoxModel();
        for (int i = 0; i < clients.size(); i++) {
            dml.addElement(clients.get(i).getName());
        }

        clientSelect.setModel(dml);
    }

    public void fetchInvoicesForSelectedClient() throws SQLException {
        String selectedClient = String.valueOf(clientSelect.getSelectedItem());

        ArrayList<Invoice> clientInvoices = invoiceDataService.GetByClient(selectedClient);

        DefaultListModel listModel = new DefaultListModel();

        for (Invoice a : clientInvoices) {
            listModel.addElement(String.valueOf(a.getDate()));
        }

        invoiceListBox.setModel(listModel);
    }
    
    
