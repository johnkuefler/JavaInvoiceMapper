    private void invoiceListBoxValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_invoiceListBoxValueChanged
        Date invoiceDate  = null;
          try {
              invoiceDate = new SimpleDateFormat("yyyy-MM-dd").parse(invoiceListBox.getSelectedValue());
          } catch (ParseException ex) {
              Logger.getLogger(InvoiceReportUI.class.getName()).log(Level.SEVERE, null, ex);
          }
        String selectedClient = String.valueOf(clientSelect.getSelectedItem());

        if (invoiceDate != null) {

            ArrayList<Sale> invoiceSales = new ArrayList<Sale>();
            try {
                invoiceSales = saleDataService.GetByInvoice(selectedClient, invoiceDate);
            } catch (SQLException ex) {
                Logger.getLogger(AgentSalesReportUI.class.getName()).log(Level.SEVERE, null, ex);
            }

            int totalChargebacks = 0;
            int totalPayable = 0;
            int totalNonPayable = 0;
            int totalSales = 0;
            float totalCommissions = 0;

            for (Sale s : invoiceSales) {
                totalSales++;
                totalCommissions+= s.getCommission();

                if (s.isChargeback()) {
                    totalChargebacks ++;
                }
                else {
                    if (s.isPayable()) {
                        totalPayable++;
                    } else {
                        totalNonPayable++;
                    }
                }
            }

            lblTotalSales.setText(String.valueOf(totalSales));
            lblPayable.setText(String.valueOf(totalPayable));
            lblNonPayable.setText(String.valueOf(totalNonPayable));
            lblChargebacks.setText(String.valueOf(totalChargebacks));
            lblCommissions.setText(String.valueOf(totalCommissions));
        }
    }//GEN-LAST:event_invoiceListBoxValueChanged

